// Функция для получения случайных вопросов из всех категорий
function getRandomQuestions(count = 10) {
    const allQuestions = [];
    
    // Собираем все вопросы из всех категорий
    Object.keys(QUIZ_DATA).forEach(category => {
        QUIZ_DATA[category].forEach(question => {
            allQuestions.push({
                ...question,
                category: category
            });
        });
    });
    
    // Перемешиваем массив
    for (let i = allQuestions.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [allQuestions[i], allQuestions[j]] = [allQuestions[j], allQuestions[i]];
    }
    
    // Возвращаем нужное количество вопросов
    return allQuestions.slice(0, count);
}